import { spawnSync } from 'node:child_process';
import { existsSync, readFileSync, readdirSync, rmSync, writeFileSync } from 'node:fs';
import { tmpdir } from 'node:os';
import { dirname, join, resolve, sep } from 'node:path';
import { fileURLToPath, pathToFileURL } from 'node:url';

const defaultSandboxRoot = resolve(join(tmpdir(), 'fluo-cli-sandbox'));
const sandboxMetadataFileName = '.fluo-cli-sandbox.json';
const scriptDirectory = dirname(fileURLToPath(import.meta.url));
const packageRoot = resolve(scriptDirectory, '..');
const repoRoot = resolve(packageRoot, '..', '..');
const representativeStarterSmokeScenarios = [
  {
    env: {},
    label: 'default Node.js + Fastify application',
    projectName: 'starter-app',
  },
  {
    env: {
      FLUO_CLI_SANDBOX_PLATFORM: 'none',
      FLUO_CLI_SANDBOX_RUNTIME: 'node',
      FLUO_CLI_SANDBOX_SHAPE: 'microservice',
      FLUO_CLI_SANDBOX_TRANSPORT: 'tcp',
    },
    label: 'TCP microservice starter',
    projectName: 'starter-microservice',
  },
  {
    env: {
      FLUO_CLI_SANDBOX_PLATFORM: 'fastify',
      FLUO_CLI_SANDBOX_RUNTIME: 'node',
      FLUO_CLI_SANDBOX_SHAPE: 'mixed',
      FLUO_CLI_SANDBOX_TRANSPORT: 'tcp',
    },
    label: 'mixed Fastify + TCP starter',
    projectName: 'starter-mixed',
  },
];

let runCliPromise;

function isPathInsideDirectory(parentDirectory, candidatePath) {
  const resolvedParentDirectory = resolve(parentDirectory);
  const resolvedCandidatePath = resolve(candidatePath);

  return resolvedCandidatePath === resolvedParentDirectory || resolvedCandidatePath.startsWith(resolvedParentDirectory + sep);
}

function resolveSandboxRoot(env) {
  const requestedSandboxRoot = env.FLUO_CLI_SANDBOX_ROOT ? resolve(env.FLUO_CLI_SANDBOX_ROOT) : undefined;

  if (requestedSandboxRoot && isPathInsideDirectory(repoRoot, requestedSandboxRoot)) {
    return {
      sandboxRoot: defaultSandboxRoot,
      warning: `Ignoring FLUO_CLI_SANDBOX_ROOT=${requestedSandboxRoot} because sandbox projects must live outside the repo workspace.`,
    };
  }

  return {
    sandboxRoot: requestedSandboxRoot ?? defaultSandboxRoot,
  };
}

const { sandboxRoot, warning: sandboxRootWarning } = resolveSandboxRoot(process.env);
const defaultProjectName = 'starter-app';

function resolveStarterArgsFromEnv(env) {
  const shape = env.FLUO_CLI_SANDBOX_SHAPE;

  if (!shape) {
    return [];
  }

  const args = ['--shape', shape];

  if (env.FLUO_CLI_SANDBOX_TRANSPORT) {
    args.push('--transport', env.FLUO_CLI_SANDBOX_TRANSPORT);
  }

  if (env.FLUO_CLI_SANDBOX_RUNTIME) {
    args.push('--runtime', env.FLUO_CLI_SANDBOX_RUNTIME);
  }

  if (env.FLUO_CLI_SANDBOX_PLATFORM) {
    args.push('--platform', env.FLUO_CLI_SANDBOX_PLATFORM);
  }

  return args;
}

function run(command, args, cwd) {
  const result = spawnSync(command, args, {
    cwd,
    stdio: 'inherit',
  });

  if (result.error) {
    throw result.error;
  }

  if (result.status !== 0) {
    throw new Error(`${command} ${args.join(' ')} failed with exit code ${result.status ?? 1}.`);
  }
}

function log(message) {
  process.stdout.write(`[cli sandbox] ${message}\n`);
}

function resolveProjectName(argv) {
  const projectName = argv[0] || defaultProjectName;

  if (projectName.includes('/') || projectName.includes('\\') || projectName === '.' || projectName === '..') {
    throw new Error('Project name must be a simple directory name without path separators.');
  }

  return projectName;
}

function resolveProjectDirectory(projectName) {
  void projectName;
  return sandboxRoot;
}

function sandboxMetadataPath(projectDirectory) {
  return join(projectDirectory, sandboxMetadataFileName);
}

function isLegacySandboxDirectory(projectDirectory) {
  return existsSync(join(projectDirectory, defaultProjectName, 'package.json'));
}

function isManagedSandboxDirectory(projectDirectory) {
  return existsSync(sandboxMetadataPath(projectDirectory));
}

function isGeneratedSandboxDirectory(projectDirectory) {
  const packageJsonPath = join(projectDirectory, 'package.json');
  const readmePath = join(projectDirectory, 'README.md');

  if (!existsSync(packageJsonPath) || !existsSync(readmePath)) {
    return false;
  }

  try {
    return readFileSync(readmePath, 'utf8').includes('Generated by @fluojs/cli.');
  } catch {
    return false;
  }
}

function assertSafeToResetSandbox(projectDirectory) {
  const resolvedProjectDirectory = resolve(projectDirectory);

  if (!existsSync(resolvedProjectDirectory)) {
    return;
  }

  if (
    isManagedSandboxDirectory(resolvedProjectDirectory)
    || isLegacySandboxDirectory(resolvedProjectDirectory)
    || isGeneratedSandboxDirectory(resolvedProjectDirectory)
  ) {
    return;
  }

  if (readdirSync(resolvedProjectDirectory).length === 0) {
    return;
  }

  throw new Error(
    `Refusing to reset a non-sandbox directory at ${resolvedProjectDirectory}. Choose an empty path or a dedicated FLUO_CLI_SANDBOX_ROOT.`,
  );
}

function writeSandboxMetadata(projectDirectory, projectName) {
  writeFileSync(
    sandboxMetadataPath(projectDirectory),
    JSON.stringify(
      {
        createdBy: '@fluojs/cli local sandbox',
        projectName,
      },
      null,
      2,
    ) + '\n',
    'utf8',
  );
}

function assertInsideSandboxRoot(projectDirectory) {
  const resolvedSandboxRoot = resolve(sandboxRoot);
  const resolvedProjectDirectory = resolve(projectDirectory);

  if (!isPathInsideDirectory(resolvedSandboxRoot, resolvedProjectDirectory)) {
    throw new Error(`Refusing to operate outside sandbox root: ${resolvedProjectDirectory}`);
  }
}

function logSandboxRoot() {
  if (sandboxRootWarning) {
    log(sandboxRootWarning);
  }

  log(`Using sandbox root ${sandboxRoot}`);
}

function cleanSandbox(projectDirectory) {
  assertInsideSandboxRoot(projectDirectory);
  assertSafeToResetSandbox(projectDirectory);
  rmSync(projectDirectory, { force: true, recursive: true });
}

function verifySandboxExists(projectDirectory) {
  if (!existsSync(join(projectDirectory, 'package.json'))) {
    throw new Error(
      `Sandbox project not found at ${projectDirectory}. Run \`pnpm sandbox:create\` first or use \`pnpm sandbox:test\`.`,
    );
  }
}

async function loadRunCli() {
  if (!runCliPromise) {
    runCliPromise = (async () => {
      log('Building @fluojs/cli dist for the local harness');
      run('pnpm', ['build'], packageRoot);

      const cliModuleUrl = pathToFileURL(join(packageRoot, 'dist', 'cli.js')).href;
      const cliModule = await import(cliModuleUrl);

      if (typeof cliModule.runCli !== 'function') {
        throw new Error('Unable to load runCli from packages/cli/dist/cli.js.');
      }

      return cliModule.runCli;
    })();
  }

  return runCliPromise;
}

async function withStarterEnv(envOverrides, action) {
  const keys = [
    'FLUO_CLI_SANDBOX_PLATFORM',
    'FLUO_CLI_SANDBOX_RUNTIME',
    'FLUO_CLI_SANDBOX_SHAPE',
    'FLUO_CLI_SANDBOX_TRANSPORT',
  ];
  const previous = new Map(keys.map((key) => [key, process.env[key]]));

  for (const key of keys) {
    const next = envOverrides[key];

    if (typeof next === 'string') {
      process.env[key] = next;
      continue;
    }

    delete process.env[key];
  }

  try {
    return await action();
  } finally {
    for (const key of keys) {
      const value = previous.get(key);

      if (typeof value === 'string') {
        process.env[key] = value;
        continue;
      }

      delete process.env[key];
    }
  }
}

async function runRepresentativeStarterSmokeMatrix() {
  for (const scenario of representativeStarterSmokeScenarios) {
    await withStarterEnv(scenario.env, async () => {
      log(`Running representative starter smoke: ${scenario.label}`);
      await createSandboxProject(scenario.projectName);
      verifySandboxProject(scenario.projectName);
    });
  }
}

async function createSandboxProject(projectName) {
  const projectDirectory = resolveProjectDirectory(projectName);
  const runCli = await loadRunCli();
  const starterArgs = resolveStarterArgsFromEnv(process.env);

  log(`Refreshing sandbox at ${projectDirectory}`);
  cleanSandbox(projectDirectory);

  const exitCode = await runCli(
    [
      'new',
      projectName,
      '--package-manager',
      'pnpm',
      '--target-directory',
      projectDirectory,
      ...starterArgs,
    ],
    {
      cwd: sandboxRoot,
      dependencySource: 'local',
      repoRoot,
    },
  );

  if (exitCode !== 0) {
    throw new Error(`runCli returned a non-zero exit code: ${exitCode}.`);
  }

  writeSandboxMetadata(projectDirectory, projectName);
  log(`Sandbox project is ready at ${projectDirectory}`);
  return projectDirectory;
}

function verifySandboxProject(projectName) {
  const projectDirectory = resolveProjectDirectory(projectName);
  verifySandboxExists(projectDirectory);
  const packageJson = JSON.parse(readFileSync(join(projectDirectory, 'package.json'), 'utf8'));
  const hasHttpStarter = Boolean(packageJson.dependencies?.['@fluojs/http']);
  const hasMicroserviceStarter = Boolean(packageJson.dependencies?.['@fluojs/microservices']);
  const starterContract = hasHttpStarter && hasMicroserviceStarter
    ? 'mixed'
    : hasMicroserviceStarter
      ? 'microservice'
      : 'application';

  if (starterContract === 'microservice' || starterContract === 'mixed') {
    if (!existsSync(join(projectDirectory, 'src', 'math', 'math.handler.test.ts'))) {
      throw new Error(`Expected the ${starterContract} starter scaffold to include src/math/math.handler.test.ts.`);
    }
  }

  if (starterContract === 'application' && !existsSync(join(projectDirectory, 'src', 'app.e2e.test.ts'))) {
    throw new Error('Expected the starter scaffold to include src/app.e2e.test.ts.');
  }

  if (!existsSync(join(projectDirectory, 'vite.config.ts'))) {
    throw new Error('Expected the starter scaffold to include vite.config.ts.');
  }

  if (starterContract === 'microservice' || starterContract === 'mixed') {
    const mainFile = readFileSync(join(projectDirectory, 'src', 'main.ts'), 'utf8');

    if (starterContract === 'microservice' && !mainFile.includes('createMicroservice')) {
      throw new Error('Expected the microservice starter main.ts to bootstrap FluoFactory.createMicroservice(...).');
    }

    if (
      starterContract === 'mixed'
      && (!mainFile.includes('await app.connectMicroservice();') || !mainFile.includes('await app.startAllMicroservices();'))
    ) {
      throw new Error('Expected the mixed starter main.ts to attach and start the TCP microservice from the HTTP app bootstrap.');
    }

  }

  for (const configPath of ['tsconfig.json', 'tsconfig.build.json', 'vite.config.ts', 'vitest.config.ts']) {
    const content = readFileSync(join(projectDirectory, configPath), 'utf8');
    if (content.includes('baseUrl')) {
      throw new Error(`Expected ${configPath} to avoid deprecated baseUrl aliasing.`);
    }
  }

  log('Running generated project checks');
  run('pnpm', ['typecheck'], projectDirectory);
  run('pnpm', ['build'], projectDirectory);
  run('pnpm', ['test'], projectDirectory);

  log('Checking installed CLI command paths inside the sandbox project');
  if (!existsSync(join(projectDirectory, 'node_modules', '.bin', 'fluo'))) {
    throw new Error('Expected the installed CLI to expose node_modules/.bin/fluo.');
  }

  run('pnpm', ['exec', 'fluo', 'g', 'repo', 'User'], projectDirectory);
  run('pnpm', ['exec', 'fluo', '--help'], projectDirectory);

  if (!existsSync(join(projectDirectory, 'src', 'users', 'user.repo.ts'))) {
    throw new Error('Expected the installed `fluo` command to generate src/users/user.repo.ts.');
  }

  if (!existsSync(join(projectDirectory, 'src', 'users', 'user.repo.slice.test.ts'))) {
    throw new Error('Expected the installed `fluo` command to generate src/users/user.repo.slice.test.ts.');
  }

  log('Re-running typecheck and test after generator output');
  run('pnpm', ['typecheck'], projectDirectory);
  run('pnpm', ['test'], projectDirectory);

  log('Sandbox verification passed');
}

function printUsage() {
  process.stdout.write(
    [
      'Usage: node ./scripts/local-test-env.mjs <create|verify|test|clean> [project-name]',
      'Use `matrix` to run the representative generated-project smoke suite (default app, TCP microservice, mixed app).',
      'Defaults project-name to starter-app and uses the sandbox root itself as the generated app directory.',
      'Set FLUO_CLI_SANDBOX_ROOT to override the sandbox root outside the repo workspace.',
    ].join('\n') + '\n',
  );
}

async function main() {
  const [command, ...rest] = process.argv.slice(2);
  const projectName = resolveProjectName(rest);
  const projectDirectory = resolveProjectDirectory(projectName);

  switch (command) {
    case 'create':
      logSandboxRoot();
      await createSandboxProject(projectName);
      break;
    case 'verify':
      logSandboxRoot();
      verifySandboxProject(projectName);
      break;
    case 'test':
      logSandboxRoot();
      await createSandboxProject(projectName);
      verifySandboxProject(projectName);
      break;
    case 'matrix':
      logSandboxRoot();
      await runRepresentativeStarterSmokeMatrix();
      break;
    case 'clean':
      logSandboxRoot();
      cleanSandbox(projectDirectory);
      log(`Removed ${projectDirectory}`);
      break;
    default:
      printUsage();
      process.exitCode = 1;
  }
}

await main();
